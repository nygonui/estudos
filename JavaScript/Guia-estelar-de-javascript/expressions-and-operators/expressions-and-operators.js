let number = 1 // Expression 

console.log(number + 1) // Operator Binary '+', precisa de dois termos para ter uma resposta 'number' e '1'
console.log(++number)// Operator Unary, de encremento, precisa apenas de uma expression para termos uma resposta
console.log(typeof number)//Operator Unary
console.log(true ? 'alo' : 'nada')// Operator Ternary, se for true, ele executára a primeira string se for false executará a segunda string